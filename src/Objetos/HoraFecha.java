package Objetos;

public class HoraFecha {
    String hora;
    String fecha;

    public HoraFecha(String hora, String fecha) {
        this.hora = hora;
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
    
}
